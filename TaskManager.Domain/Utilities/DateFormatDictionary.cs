namespace TaskManager.Domain.Utilities;

/// <summary>
/// Централизованное хранилище форматов дат и времени, используемых в приложении.
/// Обеспечивает единообразное форматирование дат во всех компонентах системы.
/// </summary>
/// <remarks>
/// Этот класс содержит константы форматов дат в соответствии со стандартами .NET.
/// Использование констант предотвращает ошибки форматирования и упрощает изменения форматов.
/// </remarks>
public class DateFormatDictionary
{
    /// <summary>
    /// Формат даты: день.месяц.год (например: 31.12.2023).
    /// Используется для краткого отображения дат в таблицах и формах.
    /// </summary>
    public const string DateFormatDdMmYyyy = "dd.MM.yyyy";

    /// <summary>
    /// Формат даты: год-месяц-день (например: 2023-12-31).
    /// Используется для HTML5 date input, сортировки и обмена данными между системами.
    /// </summary>
    public const string DateFormatYyyyMmDd = "yyyy-MM-dd";

    /// <summary>
    /// Формат даты и времени: день.месяц.год часы:минуты (например: 31.12.2023 23:59).
    /// Используется для отображения точных временных меток.
    /// </summary>
    public const string DateTimeFormatDdMmYyyyHhMm = "dd.MM.yyyy HH:mm";
}