namespace TaskManager.View.Utilities;

/// <summary>
/// Словарь CSS классов для состояния кнопок и интерактивных элементов.
/// Содержит константы для классов состояния элементов управления.
/// </summary>
public class ButtonStatesCssClassDictionary
{
    /// <summary>
    /// CSS класс для отключенного состояния кнопки.
    /// Применяется к элементам, которые временно неактивны.
    /// </summary>
    public const string Disabled = "disabled";

    /// <summary>
    /// CSS класс для активного состояния кнопки.
    /// Используется для выделения текущей активной кнопки в группе.
    /// </summary>
    public const string Active = "active";
}